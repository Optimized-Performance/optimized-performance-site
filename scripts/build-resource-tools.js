// Build the cohort-gated /resources tool bundles.
//
// Each tool in resources-tools/<slug>/ (index.html + styles.css + engine.js +
// app.js — Syngyn-skinned copies vendored from the Forged suite apps) is
// inlined into ONE self-contained HTML document and written to
// src/lib/resources/tool-html-<slug>.js as an exported string.
//
// Why a generated string module instead of fs.readFile at request time: the
// API route statically imports it, so it always bundles into the serverless
// function — no outputFileTracingIncludes, no dynamic-require tree-shake trap
// (both have bitten this repo before). The generated module is server-only
// (imported exclusively from pages/api), so tool content never enters a
// client bundle.
//
// Run after editing anything in resources-tools/:
//   node scripts/build-resource-tools.js
const fs = require('fs')
const path = require('path')

const REPO = path.join(__dirname, '..')
const SRC = path.join(REPO, 'resources-tools')
const OUT = path.join(REPO, 'src', 'lib', 'resources')

// Reports its rendered height to the parent page so the iframe grows with the
// content and the store page scrolls as one surface (no nested scrollbar).
const AUTO_HEIGHT = `<script>
(function () {
  var last = 0;
  function post() {
    var h = document.documentElement.scrollHeight;
    if (h !== last) { last = h; parent.postMessage({ type: 'syngyn-tool-height', height: h }, window.location.origin); }
  }
  if ('ResizeObserver' in window) new ResizeObserver(post).observe(document.body);
  window.addEventListener('load', post);
  setInterval(post, 800);
})();
</script>`

function must(haystack, needle, slug) {
  if (!haystack.includes(needle)) throw new Error(`${slug}: expected to find ${needle}`)
}

for (const slug of ['dosing-calculator', 'peptide-designer']) {
  const dir = path.join(SRC, slug)
  let html = fs.readFileSync(path.join(dir, 'index.html'), 'utf8')
  const css = fs.readFileSync(path.join(dir, 'styles.css'), 'utf8')
  const engine = fs.readFileSync(path.join(dir, 'engine.js'), 'utf8')
  const app = fs.readFileSync(path.join(dir, 'app.js'), 'utf8')

  must(html, '<link rel="stylesheet" href="styles.css" />', slug)
  must(html, '<script src="engine.js"></script>', slug)
  must(html, '<script src="app.js"></script>', slug)

  html = html
    .replace('<link rel="stylesheet" href="styles.css" />', `<style>\n${css}\n</style>`)
    .replace('<script src="engine.js"></script>', `<script>\n${engine}\n</script>`)
    .replace('<script src="app.js"></script>', `<script>\n${app}\n</script>${AUTO_HEIGHT}`)

  const out = `// AUTO-GENERATED by scripts/build-resource-tools.js — do not edit by hand.
// Source of truth: resources-tools/${slug}/ (edit there, then re-run the script).
export const html = ${JSON.stringify(html)}
`
  fs.mkdirSync(OUT, { recursive: true })
  fs.writeFileSync(path.join(OUT, `tool-html-${slug}.js`), out, 'utf8')
  console.log(`built src/lib/resources/tool-html-${slug}.js (${(html.length / 1024).toFixed(1)} KB)`)
}
