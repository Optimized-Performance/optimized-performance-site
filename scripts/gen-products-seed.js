// One-time / re-runnable generator: reads the current catalog out of
// src/data/products.js (by extracting the array literal as text — products.js
// uses an extensionless re-export that raw Node ESM can't resolve) and emits
// supabase-migration-products-catalog.sql: the `products` table DDL + the
// `customers.gated_access` column + idempotent seed INSERTs.
//
// Field mapping into the new gating/rails columns:
//   restricted=true        -> visibility_tier='cohort'  (else 'public')
//   durableRailsOnly=true  -> rail_policy='zelle_crypto' (legacy Zelle/crypto-only; else 'all')
//   noCoa=true             -> has_coa=false (else true)
//   array index            -> sort_order (preserve display order)
//   all existing SKUs      -> published=true (they're live today)
// New admin-added SKUs default to published=false + visibility_tier='account_gated' (fail-safe).
//
// Run: node scripts/gen-products-seed.js   (from the repo root)

const fs = require('fs')
const path = require('path')

const ROOT = path.join(__dirname, '..')
const SRC = path.join(ROOT, 'src/data/products.js')
const OUT = path.join(ROOT, 'supabase-migration-products-catalog.sql')

const src = fs.readFileSync(SRC, 'utf8')
const m = src.match(/const products\s*=\s*(\[[\s\S]*?\n\];)/)
if (!m) { console.error('Could not locate the products array literal'); process.exit(1) }
const products = eval(m[1].replace(/;\s*$/, '')) // pure data literal
if (!Array.isArray(products) || !products.length) { console.error('Empty products'); process.exit(1) }

// --- SQL value helpers ---
const sqlStr = (v) => (v == null ? 'null' : `'${String(v).replace(/'/g, "''")}'`)
const sqlNum = (v) => (v == null ? 'null' : Number(v))
const sqlBool = (v) => (v ? 'true' : 'false')
const sqlJson = (v) => (v == null ? 'null' : `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`)

const COLUMNS = [
  'id', 'sku', 'name', 'dosage', 'price', 'description', 'category', 'format',
  'vial_size', 'in_stock', 'stock', 'is_kit', 'parent_id', 'vial_count', 'purity',
  'badge', 'mw', 'half_life', 'recon_shelf_life', 'expiry', 'preorder_ship_date',
  'visibility_tier', 'rail_policy', 'has_coa', 'image_url', 'published', 'sort_order',
]

function rowValues(p, i) {
  return [
    sqlStr(p.id), sqlStr(p.sku), sqlStr(p.name), sqlStr(p.dosage), sqlNum(p.price),
    sqlStr(p.description), sqlStr(p.category), sqlStr(p.format), sqlStr(p.vialSize),
    sqlBool(p.inStock !== false), sqlNum(p.stock), sqlBool(!!p.isKit), sqlStr(p.parentId),
    sqlNum(p.vialCount), sqlNum(p.purity), sqlJson(p.badge), sqlStr(p.mw), sqlStr(p.halfLife),
    sqlStr(p.reconShelfLife), sqlStr(p.expiry), sqlStr(p.preorderShipDate),
    sqlStr(p.restricted ? 'cohort' : 'public'),
    sqlStr(p.durableRailsOnly ? 'zelle_crypto' : 'all'),
    sqlBool(!p.noCoa),
    'null',              // image_url — populated later via admin upload
    'true',              // published — existing SKUs are live
    i,                   // sort_order — preserve array order
  ].join(', ')
}

const ddl = `-- ============================================================
-- Products catalog -> DB. Migrates the catalog out of
-- src/data/products.js so SKUs are managed from admin (add / edit /
-- disable, pricing, gating, rails) with no code deploy. Pricing stays
-- server-authoritative: computeOrderTotals() reads price from HERE by id.
-- Generated by scripts/gen-products-seed.js. Idempotent (re-runnable).
-- ============================================================

create table if not exists products (
  id                 text primary key,
  sku                text unique not null,
  name               text not null,
  dosage             text,
  price              numeric(10,2) not null,
  description        text,
  category           text,
  format             text,
  vial_size          text,
  in_stock           boolean not null default true,
  stock              integer,
  is_kit             boolean not null default false,
  parent_id          text,
  vial_count         integer,
  purity             numeric,
  badge              jsonb,
  mw                 text,
  half_life          text,
  recon_shelf_life   text,
  expiry             text,
  preorder_ship_date text,
  -- gating + rails (new). Fail-safe defaults for ADMIN-added rows: hidden +
  -- most-restrictive until explicitly published. The seed below overrides
  -- these per existing SKU.
  visibility_tier    text not null default 'account_gated'
                       check (visibility_tier in ('public','cohort','account_gated')),
  rail_policy        text not null default 'all'
                       check (rail_policy in ('all','p2p_crypto','zelle_crypto')),
  has_coa            boolean not null default true,
  image_url          text,
  published          boolean not null default false,
  sort_order         integer not null default 100,
  created_at         timestamptz not null default now(),
  updated_at         timestamptz not null default now()
);

create index if not exists products_visibility_idx on products (visibility_tier) where published;
create index if not exists products_category_idx on products (category);

-- Account allowlist for the account-gated tier (grant/revoke from admin).
alter table customers add column if not exists gated_access boolean not null default false;

-- ---- Seed from products.js (${products.length} SKUs) ----
insert into products (${COLUMNS.join(', ')}) values
`

const rows = products.map((p, i) => `(${rowValues(p, i)})`).join(',\n')
const tail = `\non conflict (id) do nothing;\n`

fs.writeFileSync(OUT, ddl + rows + tail)
console.log(`Wrote ${OUT}`)
console.log(`Seeded ${products.length} SKUs.`)
console.log(`  cohort (restricted): ${products.filter(p => p.restricted).length}`)
console.log(`  zelle_crypto rails : ${products.filter(p => p.durableRailsOnly).length}`)
console.log(`  no-COA             : ${products.filter(p => p.noCoa).length}`)
